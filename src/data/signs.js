// ── Helpers ───────────────────────────────────────────────────────────

/** Get all signs */
export function getAllSigns() {
  return SIGNS;
}

/** Get signs that can be trained/recognized (static poses) */
export function getTrainableSigns() {
  return SIGNS.filter((s) => s.recognitionSupported);
}

/** Get signs for a specific module */
export function getModuleSigns(moduleId) {
  return SIGNS.filter((s) => s.module === moduleId).sort(
    (a, b) => a.moduleOrder - b.moduleOrder
  );
}

/** Get trainable signs for a specific module */
export function getModuleTrainableSigns(moduleId) {
  return SIGNS.filter((s) => s.module === moduleId && s.recognitionSupported).sort(
    (a, b) => a.moduleOrder - b.moduleOrder
  );
}

/** Get a sign by ID */
export function getSignById(id) {
  return SIGNS.find((s) => s.id === id) || null;
}

/** Backward-compatibility: GESTURES array for existing code */
export const GESTURES = getTrainableSigns();
